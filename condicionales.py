# Condicionales (if, elif, else)

age=18
if age >= 18:
    print("Es Mayor de Edad") # Separar bloques de código (como en if) mediante tabuladores, necesarios para que el código funcione

if age <18:
    print("Es menor de edad")
elif age<60:
    print("Es mayor de Edad")
else:
    print("Es adulto mayor")
